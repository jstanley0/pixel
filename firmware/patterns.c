void rainbow_pattern(unsigned char *rgb)
{
    static unsigned char cycle = 0, step = 0;
    
    switch(step)
    {
        case 0: // red to yellow
            rgb[0] = 255;
            rgb[1] = pgm_read_byte(&SIN3_RAMP[cycle]);
            rgb[2] = 0;
            break;
        case 1: // yellow to green
            rgb[0] = pgm_read_byte(&SIN3_RAMP[255 - cycle]);
            rgb[1] = 255;
            rgb[2] = 0;
            break;
        case 2: // green to cyan
            rgb[0] = 0;
            rgb[1] = 255;
            rgb[2] = pgm_read_byte(&SIN3_RAMP[cycle]);
            break;
        case 3: // cyan to blue
            rgb[0] = 0;
            rgb[1] = pgm_read_byte(&SIN3_RAMP[255 - cycle]);
            rgb[2] = 255;
            break;
        case 4: // blue to violet
            rgb[0] = pgm_read_byte(&SIN3_RAMP[cycle]);
            rgb[1] = 0;
            rgb[2] = 255;
            break;
        case 5: // violet to red
            rgb[0] = 255;
            rgb[1] = 0;
            rgb[2] = pgm_read_byte(&SIN3_RAMP[255 - cycle]);
            break;
    }
    
    if (++cycle == 0)
    {
        if (++step == 6)
            step = 0;
    }
    
}

void breathing_pattern(unsigned char *rgb)
{
    static unsigned char cycle = 0, step = 0;
    unsigned char val = pgm_read_byte(&BREATHING_RAMP[step ? 255 - cycle : cycle]);
    rgb[0] = rgb[1] = rgb[2] = val;
    if (++cycle == 0)
        step = (step + 1) & 1;
}

